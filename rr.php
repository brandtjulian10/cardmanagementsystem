<h2> <b>Rules & Regulations</b></h2>
<ul>
<li>The Aadhaar Act, 2016 states, "Every resident shall be entitled to obtain an aadhaar number by submitting his demographic information and biometric information by undergoing the process of enrolment." </li>
<li>The Act further defines residency as, "An individual who has resided in India for a period or periods amounting in all to 182 days or more in the 12 months immediately preceding the date of application for enrolment." </li>
<li>The Aadhaar Act has clarified that even if you're holding an Aadhaar or applied for one it does not mean that you have received Indian citizenship.</li>
<li>Aadhaar is a 12-digit identification number issued to an individual after the receipt of demographic information such as address, date of birth and biometric information such as iris scan, finger prints etc.</li>
<li>However, CBDT in its notification dated May 11, 2017 has exempted certain categories of individuals provided they do not hold Aadhaar and haven't applied for one. These are: <br>
(i) Non - Resident Indians (NRI) as per Income Tax Laws<br> 
(ii) Not a citizen of India <br>
(iii) Age more than 80 years or more at any time during the tax year<br> 
</li>
<li>If your residency status changes in a particular tax year say, from NRI to Resident, then you will be required to quote Aadhaar number for filing tax returns.</li>
</ul>
